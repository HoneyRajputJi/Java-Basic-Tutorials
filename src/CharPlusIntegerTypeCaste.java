public class CharPlusIntegerTypeCaste {

    public static void main(String[]args){

        char grade = 'B';
        //Encrypting by type casting
        grade = (char) (grade+8);
        System.out.println(grade);

        //De
        // Decrypting by type casting
        grade = (char) (grade-8);
        System.out.println(grade);



    }
}
